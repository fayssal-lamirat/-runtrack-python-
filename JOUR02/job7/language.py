def dev_type(langage):
    if langage == "javascript":
        print("tu es un developpeur web")
    elif langage == "python":
        print("tu es un developpeur IA")
    elif langage == "java":
        print("tu es un developpeur logiciel")
    elif langage == "reactjs":
        print("tu es un developpeur mobile")
    else:
        print("un jour, je serai le meilleur developpeur... ")
dev_type("javascript")   # affiche "tu es un developpeur web"
dev_type("python")       # affiche "tu es un developpeur IA"
dev_type("java")         # affiche "tu es un developpeur logiciel"
dev_type("reactjs")      # affiche "tu es un developpeur mobile"
dev_type("ruby")         # affiche "un jour, je serai le meilleur developpeur... "


